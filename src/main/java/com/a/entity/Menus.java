package com.a.entity;


public class Menus {

  private String mId;
  private String mName;
  private long mPrice;


  public String getMId() {
    return mId;
  }

  public void setMId(String mId) {
    this.mId = mId;
  }


  public String getMName() {
    return mName;
  }

  public void setMName(String mName) {
    this.mName = mName;
  }


  public long getMPrice() {
    return mPrice;
  }

  public void setMPrice(long mPrice) {
    this.mPrice = mPrice;
  }

}
